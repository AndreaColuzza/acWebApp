package it.geek.prenotazioni.model;

public class Prenotazione {
	
	private String cod_studente;
	private int cod_corso;
	
	
	public String getCod_studente() {
		return cod_studente;
	}
	public void setCod_studente(String cod_studente) {
		this.cod_studente = cod_studente;
	}
	public int getCod_corso() {
		return cod_corso;
	}
	public void setCod_corso(int cod_corso) {
		this.cod_corso = cod_corso;
	}
}
